    public void showTable(){
        DefaultTableModel tm = new DefaultTableModel(gr.NTs.size(),gr.terminals.size()+1);
        jTable1.setModel(tm);
        jTable1.getColumnModel().getColumn(0).setHeaderValue("NT/T");
        for (int i = 0; i < gr.terminals.size(); i++) {
            jTable1.getColumnModel().getColumn(i+1).setHeaderValue(gr.terminals.get(i).name);
        }
        
        for (int i = 0; i < gr.NTs.size(); i++) {
            String ntn = gr.NTs.get(i).name;
            jTable1.setValueAt(ntn, i, 0);
        }
        
        for (int i = 0; i < gr.NTs.size(); i++) {
            for (int j = 0; j < gr.terminals.size(); j++) {
                if(gr.tabla_M[i][j] != null)
                    jTable1.setValueAt(gr.tabla_M[i][j].string, i, j+1);
            }
        }
    }

