    public void divideGIC(){
        ArrayList<String> result = new ArrayList<String>();
        String[] part;
        for (String line : lines) {
            part = line.split("->");
            String[] div = part[1].split("\\|");
            for (String d : div) {
                result.add(part[0]+"->"+d);
            }
        }
        lines = new ArrayList<String>();
        lines = result;
        System.out.println("----------------------");
        System.out.println("Producciones Individuales");
        for (String res : lines) {
            System.out.println(res);
        }
        System.out.println("");
    }
    
    public void joinGIC(){
        ArrayList<String> result = new ArrayList<String>();
        ArrayList<String> Us, prods;
        Us = new ArrayList<String>();
        prods = new ArrayList<String>();
        
        int pos =0;
        for (String l : lines) {
            String[] p = l.split("->");            
            pos=(Us).indexOf(p[0]);
            //System.out.println("P0 : ");
            //System.out.println((int)p[0].toCharArray()[0]);
            if(pos==-1){
                Us.add(p[0]);
                prods.add(p[1]);
            }else{
                prods.set(pos,prods.get(pos)+"|"+p[1]);
            }
        }
        System.out.println("----------------------");
        System.out.println("Producciones Conjuntas");
        for (int i = 0; i < prods.size(); i++) {
            result.add(Us.get(i)+"->"+prods.get(i));
        }
        workable = new ArrayList<>();
        workable = result;
        for (String prod : workable) {
            System.out.println(prod);
        }
        System.out.println("");
    }
    
